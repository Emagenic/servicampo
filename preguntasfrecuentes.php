          <h3>Preguntas Frecuentes - Baños Quimicos</h3>
            <div class="pregunta">
              <h2>1.- Cuantos baños químicos debiese yo tener para X número de personas?</h2>
              <p>Si hablamos que van a ser utilizados para fines laborales, con una permanencia superior a una semana, y se pretende hacer un aseo de mantención semanal, se puede hablar de un máximo de 8 a 10 personas por baño. Si se hacen 2 mantenciones semanales, 11 a 14 como máximo.
              Si nos acogemos a la normativa legal descrita en el DS Nº 594 Condiciones Sanitarias y Ambientales Básicas en los Lugares de Trabajo Art. 23, nos debiésemos regir por la siguiente tabla.</p>

              
              <p>Es importante destacar lo que especifica el Artículo 22 del mismo DS:
              En los lugares de trabajo donde laboren hombres y mujeres deberón
              existir servicios higiénicos independientes y separados. Será responsabilidad del
              empleador mantenerlos protegidos del ingreso de vectores de interés sanitario, y del
              buen estado de funcionamiento y limpieza de sus artefactos.</p>
            </div>
            <div class="pregunta">
              <h2>2 - En qué consisten las mantenciones (aseos) que se realizan a los baños químicos?</h2>
              <p>Luego de haber hecho uso del baño, se dirige un vehículo de SERVICAMPO (por lo general, un camión de capacidad de carga de 2500 a 3500 kgs), equipados con estanque atmosférico, bombas de succión al vacío (asegura la extracción completa, sin dejar residuos sálidos en su interior) y un sistema de lavado con agua potable. Se hace la extracción de los restos, se lava con agua y desinfectante el interior del baño en forma íntegra, se hecha agua tanto al receptículo del wc, como al tanque destinado para lavarse las manos, en caso que lo tenga habilitado, y se procede a renovar el compuesto químico utilizado para la inhibición de olores y vectores infecciosos. Se repone el papel higiénico y el jabón, en caso que se solicite previamente.</p>
            </div>
            <div class="pregunta">
              <h2>3 - Cada cuánto tiempo (frecuencia) se debe realizar el aseo de los baños?</h2>
              <p>Esta respuesta va a depender de varios factores, pero los principales son: <br>
                <br>
              - Cantidad de gente que va a usar el baño ( cantidad aconsejada según pregunta %)<br>
              - Capacidad del receptículo de desechos<br>
              - Calidad y cantidad de producto químico aplicado en dicho depósito<br>
              - Condiciones ambientales. A mayores temperaturas se recomiendan mayores frecuencias.<br>
              - El hábito, costumbres y cuidados de quienes lo utilizan</p>
            </div>
            <div class="pregunta">
              <h2>4 - Cuales son las condiciones que debe tener el lugar donde se quiere instalar el baño químico?</h2>
              <p>El baño debe quedar situado en una superficie firme y planta. El acceso a este, debe estar despejado y firme también, te tal manera que los camiones que se dedican a realizar las mantenciones (aseos), puedan llegar a no más de 10 metros de este, ya que las mangueras que realizan la limpieza, tienen un largo limitado</p>
            </div>
            <div class="pregunta">
              <h2>5 - Qué es más conveniente, comprar un baño o arrendarlo?</h2>
              <p>Servicampo ofrece ambas opciones. Ambas son sumamente válidas, pero la conveniencia va a estar dada por cada caso.
              Si yo soy un usuario permanente de estos baños, o tengo una obra de largo aliento y/o además tengo la posibilidad de reutilizarlo en faenas futuras, sin duda conviene comprar, y pedir su aseo sólo en el momento que quieran usarlo.
              Si el lugar donde van a estar por largo tiempo, es muy extremo o lejano, dificultando y encareciendo en exceso un aseo realizado por quienes lo arriendan, también pueden ser una buena alternativa.</p>
            </div>
            <div class="pregunta">
              <h2>6 - Si yo tengo un baño químico propio (no arrendado), ustedes hacen el servicio de mantención y aseo?</h2>
              <p>Por supuesto, sólo tiene que contactarnos, y le enviamos un presupuesto.</p>
            </div>
            <div class="pregunta">
              <h2>7 - Qué tipo de baños químicos Servicampo tiene disponible para la venta?</h2>
              <p>Servicampo se especializa en la fabricación de baños para uso agrícola principalmente, sin descartar fabricar para otros sectores productivos, tales como la construcción, minería u otra área, adaptándolo a sus requerimientos.
              Fabricamos baños individuales, doble, estéticos y móviles (con ruedas), según acomode al demandante. Véase imágenes y especificaciones técnicas, en el botón productos, de la presente página</p>
            </div>
            <div class="pregunta">
              <h2>8 - Qué hacemos posteriormente con los residuos que extraemos de los baños químicos?</h2>
              <p>Servicampo tiene un contrato vigente y renovable, con empresas que se dedica al tratamiento de las aguas servidas, ajena a nuestra propiedad, y altamente regulada . Además de recepcionar las aguas provenientes de las limpiezas de las fosas domiciliarias, plantas de tratamiento o pozos, de uso corriente, procesa las desechos de los baños químicos.</p>
            </div>
            <div class="pregunta">
              <h2>9 - Cómo se yo, que estos residuos van a ser efectivamente dispuestos en una planta de tratamiento debidamente autorizada?</h2>
              <p>Servicampo es una empresa que no tiene restricciones en cuanto a los volúmenes que puedan disponerse según su contrato.
              Los camiones de Servicampo poseen equipos de rastreo por GPS, que se van alternando y monitoreando en forma aleatoria.
              Existen una serie de controles internos, asociados al proceso de mejoramiento continuo, el cual fue declarado y aplicado en nuestro proceso de Certificación Iso 9001-2008.
              Usted puede exigir que se le demuestre la trazabilidad que tenido su carga, o el comprobante que nos entrega la planta de tratamiento, en el momento de ser depositados sus desechos.</p>
            </div>
            <div class="pregunta">
              <h2>10 - Existe la posibilidad de que algún camión pueda verter sus desechos en algún canal de regadío, rio, sistema de alcantarillado u hoyo, si es que está dentro de mi propiedad o si yo considero que es agua que está limpia?</h2>
              <p>La respuesta es un NO categórico. Esa acción está altamente penada por la ley y, tanto la empresa como quien facilita cometer ese tipo de ilícitos, están expuesto a fuertes sanciones ; la cárcel es una de ellas. No tenga temor ni tedio en denunciar estos hechos; se hace un favor a usted y a todos nosotros.</p>
            </div>
            <div class="pregunta">
              <h2>11 - Qué significa la palabra RILes?</h2>
              <p>RIL es es la sigla de Residuos Industriales y Líquidos. Estas son los residuos líquidos que pueden estar contaminados, ya que fueron utilizados en alguno de los procesos productivos dentro de una fábrica, máquina o aseo de las instalaciones.</p>
            </div>
            <div class="pregunta">
              <h2>12 - Qué se considera como aguas servidas ? (domiciliarias)</h2>
              <p>Son los líquidos o lodos provenientes de fuentes tales como, baños, plantas de tratamiento de aguas servidas, duchas, lavamanos y drenes, que no tengan relación directa con procesos industriales.</p>
            </div>
            <div class="pregunta">
              <h2>13 - Cómo funcionan un baño químico?</h2>
              <p>Si usted nunca ha visto un baño químico, y menos sabe cómo funciona, se lo podríamos explicar de la siguiente manera: Imagine una caseta telefónica cerrada, con una base de un m2, y una altura de 2mts, que incluye un cajón sellado, que tiene una tapa de WC en la parte superior. A este, se le hecha un compuesto químico (líquido o en polvo), y se agrega agua, con el fin de cubrir toda la superficie del receptículo (cajón).
              Los inodoros químicos (el receptículo ya especificado) deben ser vaciados regularmente, ya que los químicos tienen un efecto sobre la inhibición sobre los olores y su función de degradar los desechos. El material extraído, debe ser trasladado a un centro de desechos adecuados, de tal forma evita la contaminación de las aguas subterráneas y por razones de seguridad e higiene.</p>
            </div>
            <div class="pregunta">
              <h2>14 - Se puede arrendar por días?</h2>
              <p>Si, se puede arrendar por días, pero debe tener en cuenta que no es proporcional al arriendo mensual. Se debe tener en consideración, que se deben asumir los costos de traslado, personal dedicado., limpieza y retiro.
              En el caso de que se arriende con pago mensual, se re-factura en forma automática, luego del 5 día de vencido el período, asumiendo que se renueva por otro mes adicional, en caso que no se de aviso en forma previa.</p>
            </div>
            <div class="pregunta">
              <h2>15 - Por qué se debe dejar un cheque de garantía al arrendar un baño?</h2>
              <p>La garantía se deja con la finalidad de asegurar que quien lo arriende, sea persona natural o empresa, haga uso correcto y está consiente del cuidado que se le debe brindar a los equipos arrendados. Los hechos de vandalismo, hurto, maltrato y extravío, ayudan a mitigar las pérdidas que sufre la empresa arrendadora.</p>
            </div>
            <div class="pregunta">
              <h2>16 - Por qué se cobran diferentes tarifas por el mismo servicio o tipo de baño?</h2>
              <p>Como todo producto o servicio, los precios de cobro, por lo general van en relación directa a los costos involucrados a la ejecución o producción.
              Cuando se arrienda un solo baño, los costos operacionales involucrados para implementar su funcionamiento, operacionalmente, difieren marginalmente del costo de arrendar 4 baños en el mismo lugar. La cantidad de operarios, los costos de traslados, los costos de aseo, etc.
              En el caso del tipo de baño, un baño estándar, es más básico en cuanto a sus accesorios, amplitud , espacios y de líneas más toscas, que un baño que se pretende usar para un evento por ejemplo. Claramente, el primero tiene un costo menor</p>
